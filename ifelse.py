# learning and practising if/else statements


my_money = 200
# If I have more than 2000, print 'I am rich' else 'I am poor'
if my_money > 2000:
    print('I am rich')
elif my_money == 200:
    print('I am doing ok')
else:
    print('I am poor')



dad = 40
mom = 42
# If my dad is older than my mom, print my dad is the oldest, else print mom is the oldest
if dad > mom:
    print('My dad is the oldest')
else:
    print('My mum is the oldest')



noise_level = 200
